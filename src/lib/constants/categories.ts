export type Subcategory = {
	slug: string;
	label: string;
};

export type Category = {
	slug: string;
	label: string;
	subcategories: Subcategory[];
};

/** @deprecated Prefer `category.slug` / `subcategory.slug`. Kept for legacy call sites. */
export const slug = (s: string) => s.replace(/\s+/g, '-').toLowerCase();

function sub(key: string): Subcategory {
	return { slug: slug(key), label: subcategoryLabel(key) };
}

function cat(key: string, keys: string[]): Category {
	return { slug: slug(key), label: categoryLabel(key), subcategories: keys.map(sub) };
}

