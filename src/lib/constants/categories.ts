export const decodeLabel = (subSlug: string): string => {
	for (const category of CATEGORIES) {
		const match = category.subcategories.find((item) => item.slug === subSlug);
		if (match) return match.label;
	}
	return subSlug
		.split('-')
		.map((word) => word.charAt(0).toUpperCase() + word.slice(1))
		.join(' ');
};

