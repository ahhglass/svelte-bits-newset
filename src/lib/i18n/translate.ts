function applyPhrases(text: string): string {
	let out = text;
	for (const [from, to] of PHRASES) {
		if (typeof from === 'string') {
			if (out.includes(from)) out = out.split(from).join(to);
		} else {
			out = out.replace(from, to);
		}
	}
	return out;
}

function translateWords(title: string): string {
	if (EXACT[title]) return EXACT[title];

	const parenMatch = title.match(/^(.+?)\s*(\([^)]+\))$/);
	if (parenMatch) {
		const base = translateWords(parenMatch[1].trim());
		const unit = parenMatch[2]
			.replace('ms', 'мс')
			.replace('deg/s', '°/с')
			.replace('px', 'px')
			.replace('s', 'с');
		return `${base} ${unit}`;
	}

	const parts = title.split(/\s+/);
	if (parts.length === 1) return WORDS[parts[0]] ?? parts[0];

	const translated = parts.map((word, i) => {
		if (/^\d+$/.test(word)) return word;
		const w = WORDS[word];
		if (!w) return word;
		if (word === 'Color' && i > 0) return w;
		return w;
	});

	let result = translated.join(' ');
	result = result.replace(/(\S)\s+(цвет|фон|контент|контейнер|компонент)$/i, (_, a, b) => {
		const adj =
			a.endsWith('ный') || a.endsWith('ой') || a.endsWith('ий')
				? a.slice(0, -2) + (b === 'цвет' ? 'ый' : b === 'фон' ? 'овый' : 'ый')
				: a;
		return `${adj} ${b}`;
	});
	return result;
}

export function translateEnglish(text: string): string {
	if (!text) return text;
	if (EXACT[text]) return EXACT[text];
	return applyPhrases(text);
}

export function translateCustomizeLabel(label: string): string {
	if (!label) return label;
	if (EXACT[label]) return EXACT[label];
	return translateWords(label);
}

export function translatePropDescription(description: string): string {
	if (!description) return description;
	if (EXACT[description]) return EXACT[description];
	const translated = applyPhrases(description);
	if (translated !== description) return translated;
	return description;
}

